package com.instagram.reels;

public class Switch_sta {
	public static void main(String[] args) {
		int ch = 3;
		switch(ch) {
		case 1: 
			System.out.println("A");
			break;
		case 2:
			System.out.println("B");
			break;
		case 3:
			System.out.println("C");
			break;
		case 4:
			System.out.println("D");
			break;
		default:
			System.out.println("another value");
		}
	}

}
