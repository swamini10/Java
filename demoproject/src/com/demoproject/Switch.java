package com.demoproject;

public class Switch {
	public static void main(String[]args) {
		switch(4) {
		case 1:
			System.out.println("A");
			break;
		case 2:
			System.out.println("B");
			break;
		case 3:
			System.out.println("C");
			break;
		case 4:
			System.out.println("D");
			break;	
			
		}
	}

}
